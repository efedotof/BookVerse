class UserModel{
  String? uid;
  String? email;
  String? name;
  String? nickname;

  UserModel({this.uid, this.email, this.name, this.nickname});

  factory UserModel.fromMap(map){
    return UserModel(uid: map['uid'],email: map['email'], name: map['name'], nickname: map['nickname']);
  }

  Map<String, dynamic> toMap(){
    return{
      'uid': uid,
      'email': email,
      'name':name,
      'nickname':nickname,
    };
  }



}